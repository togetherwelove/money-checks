const { spawnSync } = require("node:child_process");
const fs = require("node:fs");
const path = require("node:path");

const PROJECT_ROOT = path.resolve(__dirname, "..");
const PACKAGE_JSON_PATH = path.join(PROJECT_ROOT, "package.json");
const OUTPUT_PATH = path.join(PROJECT_ROOT, "docs", "OPEN_SOURCE_LICENSES.md");
const APP_OUTPUT_PATH = path.join(PROJECT_ROOT, "src", "generated", "openSourceLicenses.ts");
const PNPM_LICENSE_ARGS = ["pnpm", "licenses", "list", "--json"];
const WINDOWS_COREPACK_COMMAND = "cmd";
const WINDOWS_COREPACK_ARGS = ["/c", "corepack", ...PNPM_LICENSE_ARGS];
const UNIX_COREPACK_COMMAND = "corepack";
const GENERATED_HEADER = [
  "# 오픈소스 라이선스",
  "",
  "알뜰은 아래 오픈소스 소프트웨어를 사용합니다.",
  "",
  "이 문서는 `pnpm licenses list --json` 결과를 기준으로 자동 생성됩니다.",
  "라이선스 전문과 추가 고지 조건은 각 패키지에 포함된 `LICENSE`, `NOTICE`, `package.json` 파일을 최종 기준으로 확인해야 합니다.",
  "",
  "## 아이콘 및 이모지 사용",
  "",
  "앱의 분류 아이콘은 `@expo/vector-icons`를 통해 제공되는 Feather Icons를 사용합니다. Feather Icons는 MIT License로 제공되는 오픈소스 아이콘 세트입니다.",
  "",
  "앱에서 유니코드 이모지를 텍스트로 표시하는 경우, 이모지 이미지를 별도 에셋으로 복제하거나 배포하지 않고 각 운영체제의 기본 렌더링을 사용합니다.",
  "",
].join("\n");
const UNKNOWN_LICENSE = "Unknown";

function main() {
  const packageJson = JSON.parse(fs.readFileSync(PACKAGE_JSON_PATH, "utf8"));
  const directDependencies = new Set([
    ...Object.keys(packageJson.dependencies ?? {}),
    ...Object.keys(packageJson.devDependencies ?? {}),
  ]);
  const licenseGroups = loadPnpmLicenseGroups();
  const rows = flattenLicenseGroups(licenseGroups, directDependencies);
  const licenseSummary = summarizeLicenses(rows);
  const markdown = [
    GENERATED_HEADER,
    renderSummary(licenseSummary, rows.length),
    renderPackageTable(rows),
  ].join("\n");

  fs.mkdirSync(path.dirname(OUTPUT_PATH), { recursive: true });
  fs.writeFileSync(OUTPUT_PATH, `${markdown.trimEnd()}\n`, "utf8");
  fs.mkdirSync(path.dirname(APP_OUTPUT_PATH), { recursive: true });
  fs.writeFileSync(APP_OUTPUT_PATH, renderAppLicenseModule(markdown), "utf8");
  console.log(
    `Generated ${path.relative(PROJECT_ROOT, OUTPUT_PATH)} and ${path.relative(
      PROJECT_ROOT,
      APP_OUTPUT_PATH,
    )} with ${rows.length} packages.`,
  );
}

function loadPnpmLicenseGroups() {
  const result =
    process.platform === "win32"
      ? spawnSync(WINDOWS_COREPACK_COMMAND, WINDOWS_COREPACK_ARGS, {
          cwd: PROJECT_ROOT,
          encoding: "utf8",
        })
      : spawnSync(UNIX_COREPACK_COMMAND, PNPM_LICENSE_ARGS, {
          cwd: PROJECT_ROOT,
          encoding: "utf8",
        });

  if (result.status !== 0) {
    if (result.error) {
      throw result.error;
    }

    const stderr = result.stderr?.trim();
    throw new Error(stderr || "`pnpm licenses list --json` failed.");
  }

  return JSON.parse(result.stdout);
}

function flattenLicenseGroups(licenseGroups, directDependencies) {
  return Object.entries(licenseGroups)
    .flatMap(([licenseName, packages]) =>
      packages.map((packageInfo) => ({
        description: packageInfo.description ?? "",
        homepage: packageInfo.homepage ?? "",
        isDirect: directDependencies.has(packageInfo.name),
        license: packageInfo.license ?? licenseName ?? UNKNOWN_LICENSE,
        name: packageInfo.name,
        versions: [...(packageInfo.versions ?? [])].sort(compareText),
      })),
    )
    .sort(
      (left, right) =>
        compareText(left.name, right.name) || compareText(left.license, right.license),
    );
}

function summarizeLicenses(rows) {
  const summary = new Map();

  for (const row of rows) {
    summary.set(row.license, (summary.get(row.license) ?? 0) + 1);
  }

  return [...summary.entries()].sort(([leftLicense], [rightLicense]) =>
    compareText(leftLicense, rightLicense),
  );
}

function renderSummary(summary, packageCount) {
  return [
    "## 요약",
    "",
    `- 총 패키지 수: ${packageCount}`,
    `- 라이선스 종류: ${summary.length}`,
    "",
    "| 라이선스 | 패키지 수 |",
    "| --- | ---: |",
    ...summary.map(([licenseName, count]) => `| ${escapeMarkdown(licenseName)} | ${count} |`),
    "",
  ].join("\n");
}

function renderPackageTable(rows) {
  return [
    "## 패키지 목록",
    "",
    "| 패키지 | 버전 | 라이선스 | 직접 의존성 | 홈페이지 |",
    "| --- | --- | --- | --- | --- |",
    ...rows.map(
      (row) =>
        `| ${escapeMarkdown(row.name)} | ${escapeMarkdown(row.versions.join(", "))} | ${escapeMarkdown(row.license)} | ${row.isDirect ? "예" : ""} | ${renderHomepage(row.homepage)} |`,
    ),
    "",
  ].join("\n");
}

function renderAppLicenseModule(markdown) {
  return [
    "// This file is generated by scripts/generate-open-source-licenses.cjs.",
    "// Do not edit this file manually.",
    "",
    `export const OpenSourceLicensesText = ${JSON.stringify(markdown.trimEnd())};`,
    "",
  ].join("\n");
}

function renderHomepage(homepage) {
  if (!homepage) {
    return "";
  }

  return `<${homepage}>`;
}

function escapeMarkdown(value) {
  return String(value).replaceAll("|", "\\|").replaceAll("\n", " ");
}

function compareText(left, right) {
  return left.localeCompare(right, "en", { sensitivity: "base" });
}

main();
